function reviver(key: any, value: any) {
  if (typeof value === 'object' && value !== null) {
    if (value.dataType === 'Map') {
      return new Map(value.value);
    }
  }
  return value;
}

function parseJSONwithES6Map(input: string) {
  return JSON.parse(input, reviver);
}

